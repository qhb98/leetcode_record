"""
=================================================================
输入描述: 输入有多组测试用例, 每组空格隔开两个整数
输入例子:
    1 1
==================================================================
输出描述: 对于每组数据输出一行两个整数的和
输出例子:
    2
==================================================================
"""

while True:
    try:
        num = list(map(int, input().split(" ")))
        print(sum(num))
    except:
        break
