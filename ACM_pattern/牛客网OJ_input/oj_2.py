"""
=================================================================
输入描述: 输入第一行包括一个数据组数t, 接下来每行包括两个正整数a, b
输入例子:
    2
    1 5
    10 20
==================================================================
输出描述: 输出a+b的结果
输出例子:
    6
    30
==================================================================
"""

t = int(input())
for i in range(t):
    num = list(map(int, input().split(" ")))
    print(sum(num))
